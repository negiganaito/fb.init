const setHostSubdomain = (host, subdomain) => {
  const parts = host.split(".");
  if (parts.length < 3) {
    parts.unshift(subdomain);
  } else {
    parts[0] = subdomain;
  }
  return parts.join(".");
};

export default setHostSubdomain;

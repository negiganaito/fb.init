function coerceImageishURL(image) {
  if (
    image &&
    typeof image === "object" &&
    !image.sprited &&
    typeof image.uri === "string" &&
    image.width !== undefined &&
    image.height !== undefined
  ) {
    return image;
  } else {
    return null;
  }
}

export default coerceImageishURL;

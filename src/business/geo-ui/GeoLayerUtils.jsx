export function mapAlign(align) {
  switch (align) {
    case "left":
      return "start";
    case "center":
      return "middle";
    case "right":
      return "end";
    default:
      throw new Error("Unknown align");
  }
}

export function mapPosition(position) {
  switch (position) {
    case "below":
      return "below";
    case "above":
      return "above";
    case "left":
      return "start";
    case "right":
      return "end";
    default:
      throw new Error("Unknown position");
  }
}

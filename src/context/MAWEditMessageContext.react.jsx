import React, { createContext, useMemo, useState } from "react";
import emptyFunction from "fbjs/lib/emptyFunction";

const Context = createContext({
  editMessageData: null,
  setEditMessageData: emptyFunction,
});

const MAWEditMessageContext = ({ children }) => {
  const [editMessageData, setEditMessageData] = useState(null);

  const value = useMemo(
    () => ({ editMessageData, setEditMessageData }),
    [editMessageData, setEditMessageData]
  );

  return <Context.Provider value={value}>{children}</Context.Provider>;
};

MAWEditMessageContext.displayName = MAWEditMessageContext.name;

export { Context, MAWEditMessageContext };

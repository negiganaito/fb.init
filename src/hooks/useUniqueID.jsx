import { useRef } from "react";

import uniqueID from "../business/helpers/uniqueID";

const useUniqueID = () => {
  const idRef = useRef(null);

  if (idRef.current === null) {
    idRef.current = uniqueID();
  }

  return idRef.current;
};

export default useUniqueID;

import React from "react";

import { isFbt } from "./GeoFbtUtils";
import { makeGeoComponent } from "./GeoPrivateMakeComponent";
import GeoTooltip from "./GeoTooltip.react";

const GeoPrivateFbtOrTooltip = ({ children }) => {
  if (children === null) return null;

  return isFbt(children) ? <GeoTooltip content={children} /> : { children };
};

GeoPrivateFbtOrTooltip.displayName = `${GeoPrivateFbtOrTooltip.name}`;

const GeoPrivateFbtOrTooltipComponent = makeGeoComponent(
  "GeoPrivateFbtOrTooltip",
  GeoPrivateFbtOrTooltip
);

export default GeoPrivateFbtOrTooltipComponent;

import React from "react";

import Link from "../../faang/components/Link.react";
import { useApplyGeoDomIDsDirectly } from "../geo-ui/GeoDomID";

const AbstractSidebarDefaultRouterLink = (props) => {
  const {
    children,
    "aria-describedby": describedBy,
    "aria-labelledby": labelledBy,
    ...restProps
  } = props;

  const geoDomProps = useApplyGeoDomIDsDirectly({
    "aria-describedby": describedBy ?? undefined,
    "aria-labelledby": labelledBy ?? undefined,
  });

  const { ref, ...geoProps } = geoDomProps;

  return (
    <Link linkRef={ref} {...restProps} {...geoProps}>
      {children}
    </Link>
  );
};

AbstractSidebarDefaultRouterLink.displayName =
  "AbstractSidebarDefaultRouterLink";

export default AbstractSidebarDefaultRouterLink;

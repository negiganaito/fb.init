import React, { useContext } from "react";

import AbstractSidebarNavigationDisplayContext from "../contexts/AbstractSidebarNavigationDisplayContext";

import BizKitSidebarItemIconBadge from "./BizKitSidebarItemIconBadge.react";

const BizKitSidebarItemIcon = ({
  badge,
  icon,
  isItemActive,
  isItemHovered,
}) => {
  const { isCollapsed } = useContext(AbstractSidebarNavigationDisplayContext);

  return (
    <>
      {icon}
      {isCollapsed && (
        <BizKitSidebarItemIconBadge
          isCollapsed={isCollapsed}
          isItemActive={isItemActive}
          isItemHovered={isItemHovered}
          badge={badge}
        />
      )}
    </>
  );
};

BizKitSidebarItemIcon.displayName = `${BizKitSidebarItemIcon.name}`;

export default BizKitSidebarItemIcon;

import { useEffect } from "react";

import { ReactUseEvent_React } from "./ReactUseEvent.react"; // Replace with correct path

export function useKeyboard(ref, { disabled = false, onKeyDown, onKeyUp }) {
  const keyDownEvent = ReactUseEvent_React("keydown");
  const keyUpEvent = ReactUseEvent_React("keyup", { passive: true });

  useEffect(() => {
    const element = ref.current;

    if (element !== null) {
      keyDownEvent.setListener(
        element,
        !disabled && onKeyDown ? onKeyDown : null
      );
      keyUpEvent.setListener(element, !disabled && onKeyUp ? onKeyUp : null);
    }
  }, [disabled, onKeyDown, onKeyUp, keyDownEvent, keyUpEvent, ref]);
}

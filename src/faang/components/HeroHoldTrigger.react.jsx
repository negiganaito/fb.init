import { useContext, useLayoutEffect } from "react";

import HeroInteractionContext from "../../context/CometHeroInteractionContext";
import HeroInteractionIDContext from "../../context/HeroInteractionIDContext";

const HeroHoldTrigger = ({ description, hold }) => {
  const interactionContext = useContext(HeroInteractionContext);
  const interactionId = useContext(HeroInteractionIDContext);

  useLayoutEffect(() => {
    if (hold && interactionId !== null) {
      const holdId = interactionContext.hold(
        interactionId,
        interactionContext.pageletStack,
        description
      );
      return () => {
        interactionContext.unhold(interactionId, holdId);
      };
    }
  }, [description, hold, interactionContext, interactionId]);

  return null;
};

HeroHoldTrigger.displayName = "HeroHoldTrigger";

export default HeroHoldTrigger;

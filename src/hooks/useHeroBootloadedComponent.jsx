import { useContext, useEffect } from "react";

import CometHeroInteractionContext from "../context/CometHeroInteractionContext";

export default function useHeroBootloadedComponent(component) {
  const heroInteractionContext = useContext(
    CometHeroInteractionContext.HeroInteractionContext.Context
  );

  useEffect(() => {
    heroInteractionContext.consumeBootload(component.getModuleId());
  }, [heroInteractionContext, component]);
}

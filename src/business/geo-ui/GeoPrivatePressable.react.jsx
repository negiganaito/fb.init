import React from "react";

import WebPressable from "../../faang/components/WebPressable.react";
import useGeoPrivatePressableSSRSafeProps from "../hooks/useGeoPrivatePressableSSRSafeProps";

import GeoPrivatePressableRouterLink from "./GeoPrivatePressableRouterLink";

const GeoPrivatePressable = (props) => {
  const safeProps = useGeoPrivatePressableSSRSafeProps(props);

  return safeProps.link !== null ? (
    <GeoPrivatePressableRouterLink {...safeProps} />
  ) : (
    <WebPressable {...safeProps} />
  );
};

GeoPrivatePressable.displayName = `GeoPrivatePressable [from ${module.id}]`;

export default GeoPrivatePressable;

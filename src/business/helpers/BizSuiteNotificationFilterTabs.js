import createEnum from "./$InternalEnum";

const getLabelForTab = (tab) => {
  switch (tab) {
    case FilterTabs.BUSINESS:
      return "BIZ_CHROME_ACCOUNT";
    case FilterTabs.ADS:
      return "BIZ_CHROME_ADS";
    case FilterTabs.PROFILES:
      return "BIZ_CHROME_PROFILE_PLUS";
    default:
      return null;
  }
};

const FilterTabs = createEnum.Mirrored([
  "ALL",
  "PROFILES",
  "BUSINESS",
  "ADS",
  "FACEBOOK",
  "INSTAGRAM",
  "HIGH_PRIORITY",
  "OTHER_PRIORITY",
]);

export { FilterTabs, getLabelForTab };

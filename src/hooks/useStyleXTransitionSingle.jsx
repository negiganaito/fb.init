import useStyleXTransition from "./useStyleXTransition";

function useStyleXTransitionSingle(item, options) {
  const transition = useStyleXTransition(
    item !== null ? [item] : [],
    () => 0,
    options
  );
  return transition[0];
}

export default useStyleXTransitionSingle;

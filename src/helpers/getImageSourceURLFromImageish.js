function getImageSourceURLFromImageish(imageish) {
  if (typeof imageish === "string") {
    return imageish;
  }

  if (
    imageish !== null &&
    typeof imageish === "object" &&
    !imageish.sprited &&
    imageish.uri &&
    typeof imageish.uri === "string"
  ) {
    return imageish.uri;
  }

  return "";
}

export default getImageSourceURLFromImageish;
